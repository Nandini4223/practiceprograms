
public class Student {
	private int rollnum;
	private String name;
	public Student(int rollnum, String name) {
		super();
		this.rollnum = rollnum;
		this.name = name;
		//System.out.println("students arg con");
	}
	public int getRollNum() {
		return rollnum;
	}
	public String getName() {
		return name;
		
	}

}
